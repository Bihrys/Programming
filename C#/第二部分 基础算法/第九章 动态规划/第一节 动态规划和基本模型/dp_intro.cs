/*
第二部分 第九章 第一节 动态规划和基本模型

一、DP 四步法
1) 定义状态：dp[i] 表示什么。
2) 写转移：dp[i] 如何从更小状态得到。
3) 初始化边界。
4) 提取答案。

二、习题
1) 台阶问题：每次走 1 或 2 步，求到第 n 阶的方法数。
2) 解释为什么这个问题可用 DP。

三、答案与讲解
1) dp[i] = dp[i-1] + dp[i-2]。
2) 因为有重叠子问题 + 最优子结构。
*/
using System; // 输出

class Program
{
    static void Main()
    {
        int n = 10; // 目标台阶数
        long[] dp = new long[n + 1]; // 定义状态数组
        dp[0] = 1; // 到 0 阶方法数为 1（什么都不做）
        dp[1] = 1; // 到 1 阶方法数为 1
        for (int i = 2; i <= n; i++) // 从小到大推状态
        {
            dp[i] = dp[i - 1] + dp[i - 2]; // 状态转移
        }
        Console.WriteLine(dp[n]); // 输出答案
    }
}
