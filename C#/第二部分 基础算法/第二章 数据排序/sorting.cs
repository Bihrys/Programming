2) 尝试使用 Array.Sort 并比较代码长度。

三、答案与讲解
1) 双重循环，逆序对交换。
2) 工程中优先标准库，竞赛中要会手写核心算法。
*/
using System; // 输出与数组

class Program
{
    static void Main()
    {
        int[] a = { 5, 1, 4, 2, 8, 3 }; // 待排序数组
        for (int i = 0; i < a.Length - 1; i++) // 控制轮数
        {
            for (int j = 0; j < a.Length - 1 - i; j++) // 相邻比较
            {
                if (a[j] > a[j + 1]) // 若逆序则交换
                {
                    (a[j], a[j + 1]) = (a[j + 1], a[j]); // 元组交换写法
                }
            }
        }
        Console.WriteLine(string.Join(" ", a)); // 输出排序结果
    }
}
