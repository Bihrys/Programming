/*
第二部分 第三章 递推算法

一、原理
1) 递推 = 已知小规模答案，逐步推出大规模答案。
2) 关键是状态定义和转移方程。
3) 常见例子：斐波那契、台阶计数。

二、习题
1) 输出前 20 项斐波那契。
2) 把数组递推改成滚动变量节省空间。

三、答案与讲解
1) f[i] = f[i-1] + f[i-2]。
2) 只保留前两项即可把空间降到 O(1)。
*/
using System; // 输出库

class Program
{
    static void Main()
    {
        int n = 10; // 目标项
        long[] f = new long[n + 1]; // 递推数组
        f[1] = 1; // 初值
        f[2] = 1; // 初值
        for (int i = 3; i <= n; i++) // 从第3项开始递推
        {
            f[i] = f[i - 1] + f[i - 2]; // 递推公式
        }
        Console.WriteLine(f[n]); // 输出第 n 项
    }
}
