/*
第三部分 第四章 第三节 最短路径算法（Dijkstra）

一、原理
1) 适用于“边权非负”的单源最短路。
2) 核心步骤：取当前未确定最短的点，松弛它的邻边。
3) 可用优先队列优化到 O((V+E)logV)。

二、习题
1) 输出起点到所有点最短距离。
2) 记录前驱并恢复路径。

三、答案与讲解
1) dist[] 初始化为无穷大，起点为 0。
2) 松弛成功时更新前驱数组。
*/
using System; // 输出

class Program
{
    static void Main()
    {
        int[] dist = { 0, 2, 3, 4, 5 }; // 示例最短路结果（简化展示）
        Console.WriteLine($"dist_to_4={dist[4]}"); // 输出到 4 号点距离
    }
}
