/*
第4章 第4节 循环嵌套

一、概念
1) 在一个循环体内部再写循环。
2) 典型场景：二维数组遍历、乘法表。

二、复杂度提醒
1) 双层循环通常是 O(n^2)。
2) 写之前先估算规模，防止超时。

三、习题
1) 打印九九乘法表。
2) 打印 n 行星号三角形。

四、答案与讲解
1) 外层控制行，内层控制列。
2) 每行输出数量与行号有关。
*/
using System; // 控制台输出

class Program // 类定义
{
    static void Main() // 主函数
    {
        for (int i = 1; i <= 9; i++) // 外层控制行
        {
            for (int j = 1; j <= i; j++) // 内层控制列
            {
                Console.Write($"{j}*{i}={i * j,2} "); // 输出一个乘法表达式
            }
            Console.WriteLine(); // 每行结束换行
        }
    } // Main 结束
} // 类结束
