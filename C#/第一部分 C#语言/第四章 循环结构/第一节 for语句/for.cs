/*
第4章 第1节 for语句

一、语法
for (初始化; 条件; 更新) { 循环体 }

二、原理
1) 初始化只执行一次。
2) 每轮先判断条件，再执行循环体，最后执行更新。
3) 适合“循环次数明确”的场景。

三、习题
1) 计算 1 到 100 的和。
2) 输出 1 到 n 的偶数。

四、答案与讲解
1) 用 sum += i 累加。
2) if (i % 2 == 0) 输出。
*/
using System; // 控制台输出

class Program // 类定义
{
    static void Main() // 主函数
    {
        int sum = 0; // 定义累加变量
        for (int i = 1; i <= 100; i++) // i 从 1 到 100
        {
            sum += i; // 每轮累加 i
        }
        Console.WriteLine($"sum={sum}"); // 输出总和
    } // Main 结束
} // 类结束
