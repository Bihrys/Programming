/*
第2章 第1节 赋值语句

一、基础原理
1) 赋值的本质：把右侧表达式计算出的值，写入左侧变量。
2) 复合赋值（+=、-=、*=、/=）是简写形式。
3) C# 中变量必须先声明再使用。

二、习题
1) 让 x 从 10 变成 25 并输出。
2) 使用临时变量交换 a 和 b。

三、答案与讲解
1) x += 15;
2) t = a; a = b; b = t;
*/
using System; // 引入 Console

class Program // 定义程序类
{
    static void Main() // 主函数
    {
        int x = 5; // 定义整型变量 x，初值 5
        x = x + 3; // 普通赋值，先算右侧
        x += 2; // 复合赋值，等价于 x = x + 2
        Console.WriteLine($"x = {x}"); // 输出结果
    } // 主函数结束
} // 类结束
