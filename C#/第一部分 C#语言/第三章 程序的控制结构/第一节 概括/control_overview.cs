/*
第3章 第1节 概括

一、程序控制三大结构
1) 顺序结构：按顺序执行。
2) 分支结构：按条件选择路径（if/switch）。
3) 循环结构：重复执行（for/while/do-while）。

二、原理
1) 你可以把程序理解成“流程图”。
2) 写代码前先想输入、处理、输出，再映射到控制结构。

三、习题
1) 用伪代码描述“买饮料”流程。
2) 分析“先执行后判断”和“先判断后执行”的差异。

四、答案与讲解
1) 余额足够才购买，否则提示余额不足。
2) do-while 至少执行一次，while 不保证执行。
*/
using System; // 引入控制台输出

class Program // 类定义
{
    static void Main() // 主入口
    {
        int x = 5; // 初始化变量
        if (x > 0) // 条件判断
        {
            Console.WriteLine("x 大于 0"); // 分支输出
        }
        Console.WriteLine("程序继续执行"); // 顺序结构继续
    }
}
