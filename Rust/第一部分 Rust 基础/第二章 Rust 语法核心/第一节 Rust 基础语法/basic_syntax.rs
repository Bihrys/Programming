/*
第一部分 第二章 第一节 Rust 基础语法

一、变量与可变性
1) let 声明不可变变量。
2) let mut 声明可变变量。
3) Rust 默认不可变，减少意外修改风险。

二、常量与重影
1) const 声明编译期常量，必须标注类型。
2) let 同名可重影（shadowing），是“重新绑定”，不是修改原变量。

三、语句与表达式
1) 语句执行动作，不返回值。
2) 表达式可产生值。
3) 函数最后一行无分号即返回表达式值。

四、练习题
1) 写出不可变变量、可变变量、常量三个示例。
2) 用重影把字符串长度变成整数并输出。
*/
const MAX_SCORE: i32 = 100; // 定义常量，必须显式类型

fn main() {
    let a = 123; // 不可变变量
    let mut b = 10; // 可变变量
    b = 20; // 修改可变变量
    println!("a = {}, b = {}, MAX_SCORE = {}", a, b, MAX_SCORE); // 输出变量值

    let x = "12345"; // 第一次绑定，x 是字符串切片
    let x = x.len(); // 重影：第二次绑定，x 变成 usize
    println!("重影后的 x = {}", x); // 输出重影结果
}

