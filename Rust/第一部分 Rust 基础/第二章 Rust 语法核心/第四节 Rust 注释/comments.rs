/// 计算两个 i32 整数的和。
/// # 参数
/// - a: 第一个加数
/// - b: 第二个加数
/// # 返回
/// 两数之和
fn add(a: i32, b: i32) -> i32 {
    a + b // 直接返回表达式值
}

fn main() {
    let result = add(3, 5); // 调用 add 函数
    println!("result = {}", result); // 输出结果
}

