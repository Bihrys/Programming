/*
第5章 第1节 一维数组
核心点:
1) 数组是同类型元素的连续存储。
2) 下标从0开始。
3) 常见操作: 遍历、统计、查找。

习题:
1) 输入n个数输出最大最小值。
2) 统计数组中偶数个数。

答案与讲解:
1) 一次遍历同时维护max/min即可。
2) arr[i] % 2 == 0时计数加1。
*/
#include <stdio.h> // 输入输出

int main(void) { // 主函数开始
    int arr[5] = {2, 4, 1, 9, 7}; // 定义并初始化一维数组
    int mx = arr[0]; // 最大值初始为首元素
    int mn = arr[0]; // 最小值初始为首元素
    for (int i = 1; i < 5; ++i) { // 从第2个元素开始遍历
        if (arr[i] > mx) mx = arr[i]; // 更新最大值
        if (arr[i] < mn) mn = arr[i]; // 更新最小值
    } // 遍历结束
    printf("max=%d min=%d\n", mx, mn); // 输出统计结果
    return 0; // 正常结束
} // main结束
