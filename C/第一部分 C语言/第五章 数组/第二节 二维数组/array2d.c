/*
第5章 第2节 二维数组
核心点:
1) 二维数组可理解为表格或矩阵。
2) 访问形式: a[行][列]。
3) 常用双重循环遍历。

习题:
1) 求3x3矩阵主对角线和。
2) 输出矩阵转置。

答案与讲解:
1) 累加a[i][i]即可。
2) 输出时交换下标a[j][i]。
*/
#include <stdio.h> // 标准输入输出

int main(void) { // 主函数开始
    int a[2][3] = {{1, 2, 3}, {4, 5, 6}}; // 定义二维数组
    for (int i = 0; i < 2; ++i) { // 外层控制行
        for (int j = 0; j < 3; ++j) { // 内层控制列
            printf("%d ", a[i][j]); // 输出当前元素
        } // 内层结束
        printf("\n"); // 每行结束换行
    } // 外层结束
    return 0; // 正常结束
} // main结束
