/*
第8章 第4节 指针与函数
核心点:
1) 函数名可视为函数地址。
2) 函数指针可保存函数地址并调用。
3) 常用于回调、策略切换。

习题:
1) 写加减乘函数并用函数指针调用。
2) 写简单计算器(运算符映射函数指针)。

答案与讲解:
1) int (*fp)(int,int)=add;
2) 根据运算符选择对应函数指针。
*/
#include <stdio.h> // 标准输入输出

int add(int a, int b) { // 定义普通加法函数
    return a + b; // 返回加法结果
} // add结束

int main(void) { // 主函数开始
    int (*fp)(int, int) = add; // 定义函数指针并指向add
    printf("%d\n", fp(3, 4)); // 通过函数指针调用函数
    return 0; // 正常结束
} // main结束
