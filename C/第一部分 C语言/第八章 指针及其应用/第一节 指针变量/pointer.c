/*
第8章 第1节 指针变量
核心点:
1) 指针存储地址。
2) &取地址, *解引用。
3) 指针必须指向有效地址后再解引用。

习题:
1) 用指针修改x的值。
2) 输出x地址并解释含义。

答案与讲解:
1) int *p=&x; *p=新值;
2) 地址代表变量在内存中的位置。
*/
#include <stdio.h> // 标准输入输出

int main(void) { // 主函数开始
    int x = 10; // 定义普通变量x
    int *p = &x; // 定义指针p并保存x地址
    *p = 20; // 通过解引用修改x的值
    printf("x=%d\n", x); // 输出x新值
    return 0; // 正常结束
} // main结束
