/*
第8章 第2节 指针与数组
核心点:
1) 数组名常退化为首元素地址。
2) arr[i] 等价于 *(arr+i)。
3) 指针遍历数组效率高且常见。

习题:
1) 用指针遍历数组并求和。
2) 比较arr[i]与*(arr+i)写法。

答案与讲解:
1) 定义int *p=arr, 循环累加*(p+i)。
2) 两种写法等价。
*/
#include <stdio.h> // 标准输入输出

int main(void) { // 主函数开始
    int arr[4] = {1, 2, 3, 4}; // 定义数组
    int *p = arr; // 指针指向数组首元素
    int sum = 0; // 定义求和变量
    for (int i = 0; i < 4; ++i) { // 循环遍历数组下标
        sum += *(p + i); // 指针偏移访问元素并累加
    } // 循环结束
    printf("sum=%d\n", sum); // 输出数组元素和
    return 0; // 正常结束
} // main结束
