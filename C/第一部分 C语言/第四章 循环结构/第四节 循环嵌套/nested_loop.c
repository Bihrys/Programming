/*
第4章 第4节 循环嵌套
核心点:
1) 一个循环体内再放一个循环。
2) 常用于二维遍历与乘法表。
3) 注意时间复杂度通常上升为O(n^2)。

习题:
1) 打印九九乘法表。
2) 打印n行星号三角形。

答案与讲解:
1) 外层控制行, 内层控制列。
2) 内层循环控制每行输出数量。
*/
#include <stdio.h> // 输出函数声明

int main(void) { // 主函数开始
    for (int i = 1; i <= 9; ++i) { // 外层循环控制行
        for (int j = 1; j <= i; ++j) { // 内层循环控制列
            printf("%d*%d=%2d ", j, i, i * j); // 输出一个乘法式
        } // 内层循环结束
        printf("\n"); // 每行输出完换行
    } // 外层循环结束
    return 0; // 正常结束
} // main结束
