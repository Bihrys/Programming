/*
第2章 第1节 赋值语句
核心点:
1) = 表示把右侧结果赋值给左侧变量。
2) 复合赋值: += -= *= /=。
3) 赋值前变量必须先声明。

习题:
1) 令x初值为10, 再把x增加15并输出。
2) 用临时变量交换a和b。

答案与讲解:
1) x += 15; printf("%d", x);
2) t=a; a=b; b=t;
*/
#include <stdio.h> // 使用printf输出结果

int main(void) { // 主函数开始
    int x = 5; // 声明整型变量x并初始化为5
    x = x + 3; // 普通赋值: 先算x+3, 再写回x
    x += 2; // 复合赋值: 等价于x = x + 2
    printf("x = %d\n", x); // 输出x当前值
    return 0; // 正常结束程序
} // 主函数结束
