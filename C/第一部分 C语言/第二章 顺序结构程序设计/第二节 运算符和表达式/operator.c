/*
第2章 第2节 运算符和表达式
核心点:
1) 算术运算符: + - * / %。
2) 关系运算符: > < >= <= == !=。
3) 逻辑运算符: && || !。
4) 不确定优先级时加括号。

习题:
1) 输入a和b, 输出a+b、a-b、a*b、a/b。
2) 判断整数n是否在[1,100]内。

答案与讲解:
1) 先scanf读取, 再printf输出各表达式。
2) 条件写成: (n >= 1 && n <= 100)。
*/
#include <stdio.h> // 提供输入输出函数

int main(void) { // 主函数开始
    int a = 10; // 定义a并赋初值
    int b = 3; // 定义b并赋初值
    int sum = a + b; // 加法表达式
    int mod = a % b; // 取模表达式
    int ok = (a > b) && (b > 0); // 逻辑与表达式
    printf("sum=%d mod=%d ok=%d\n", sum, mod, ok); // 一次输出多个结果
    return 0; // 程序结束
} // 主函数结束
