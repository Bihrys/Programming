/*
第2章 第5节 数据输入输出
核心点:
1) scanf读取输入, printf输出结果。
2) 格式控制符要和变量类型匹配。
3) 输入失败要及时处理。

习题:
1) 输入姓名和年龄并输出。
2) 输入两个整数输出和与平均值。

答案与讲解:
1) 字符串可用%s读取(注意缓冲区长度)。
2) 平均值建议转double避免整除丢失。
*/
#include <stdio.h> // 标准输入输出

int main(void) { // 主函数开始
    char name[32]; // 定义字符数组保存姓名
    int age = 0; // 定义年龄变量
    printf("请输入姓名和年龄: "); // 提示用户输入
    if (scanf("%31s%d", name, &age) != 2) { // 检查是否成功读取2项
        printf("输入格式错误\n"); // 输入失败提示
        return 1; // 非0表示异常结束
    } // 输入检查结束
    printf("name=%s age=%d\n", name, age); // 输出读取到的数据
    return 0; // 正常结束
} // 主函数结束
