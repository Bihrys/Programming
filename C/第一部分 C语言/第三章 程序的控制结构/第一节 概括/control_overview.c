/*
第3章 第1节 概括
三大控制结构:
1) 顺序结构: 代码按书写顺序执行。
2) 选择结构: 根据条件选择分支。
3) 循环结构: 重复执行某段代码。

习题:
1) 写出“买饮料”流程的伪代码。
2) 写出一个“先判断后执行”的例子。

答案与讲解:
1) 先判断余额是否足够, 再购买。
2) 典型是if/while语句。
*/
#include <stdio.h> // 引入输出功能

int main(void) { // 主函数开始
    int x = 5; // 初始化变量x
    if (x > 0) { // 选择结构: 条件为真进入分支
        printf("x > 0\n"); // 输出判断结果
    } // if分支结束
    printf("程序继续执行\n"); // 顺序结构继续执行
    return 0; // 程序结束
} // 主函数结束
