#!/usr/bin/env bash

: <<'DOC'
第2章 第4节 位置参数与特殊变量

一、位置参数
1) $1 $2 ...：第1个、第2个参数
2) $#：参数个数
3) $@：所有参数（逐个保留）

二、常见特殊变量
1) $0：脚本名称
2) $?：上一个命令退出码
3) $$：当前脚本进程 ID

三、习题
1) 传入两个参数，输出参数个数和参数值。
2) 打印当前脚本 PID 和上个命令退出码。

四、答案与讲解
1) 用 $# 和 "$1" "$2" 读取。
2) $$ 取 PID；执行命令后用 $? 取退出码。
DOC

echo "script=$0" # 输出脚本名
echo "arg_count=$#" # 输出参数个数
echo "all_args=$*" # 输出全部参数（字符串形式）
echo "first_arg=${1:-无}" # 输出第1个参数，无则显示“无”
echo "second_arg=${2:-无}" # 输出第2个参数，无则显示“无”
echo "pid=$$" # 输出当前进程ID

true # 执行一个成功命令
echo "last_exit_code=$?" # 输出上一条命令退出码

